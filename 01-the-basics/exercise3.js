// 'true'
// data type = 'string'
console.log(typeof('string'));

// false
// data type = boolean
console.log(typeof(false));

// 1.5
// data type = number
console.log(typeof(1.5));

// 2
// data type = number
console.log(typeof(2));

// undefined
// data type = undefined
console.log(typeof(undefined));

// { foo: 'bar' }
// data type = object
console.log(typeof({ foo: 'bar' }));